function isTerminalGenerationStatus(status: SuiteGenerationStatus): boolean {
  return status === 'complete'
    || status === 'partial'
    || status === 'cancelled'
    || status === 'error';
}

function formatGenerationStartedAt(startedAt: number): string {
  return new Date(startedAt * 1000).toLocaleString([], {
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit',
    hour12: false,
  });
}

function getReviewerStatusIcon(status: SuiteReviewerStatus['status']): string {
  switch (status) {
    case 'working':
      return '⏱';
    case 'done':
      return '✓';
    case 'retry':
      return '⟳';
    case 'failed':
    case 'skipped':
      return '⚠';
    default:
      return '○';
  }
}

function humanJoin(values: string[]): string {
  if (values.length <= 1) {
    return values[0] ?? '';
  }
  if (values.length === 2) {
    return `${values[0]} and ${values[1]}`;
  }
  return `${values.slice(0, -1).join(', ')}, and ${values[values.length - 1]}`;
}

function formatCoverageModeLabel(mode: string | undefined): string {
  switch (mode) {
    case 'strict_leaf_coverage':
      return 'strict leaf coverage';
    case 'compact_leaf_coverage':
      return 'compact leaf coverage';
    case 'group_coverage':
      return 'group coverage';
    default:
      return mode ?? 'custom coverage';
  }
}

function formatCriteriaDepthLabel(depth: string | undefined): string {
  switch (depth) {
    case 'detailed':
      return 'detailed criteria';
    case 'basic':
      return 'basic criteria';
    default:
      return depth ? `${depth} criteria` : 'standard criteria';
  }
}

function buildFriendlyGeneratedSuiteDescription(
  suite: PromptSuite,
  metadata: SuiteGenerationMetadata,
): string {
  const actualCount = suite.item_count ?? metadata.saved_question_count ?? suite.items.length ?? metadata.requested_question_count;
  const parts: string[] = [];
  if (metadata.topic) {
    if (actualCount != null) {
      parts.push(`${actualCount} ${actualCount === 1 ? 'question' : 'questions'} on ${metadata.topic}.`);
    } else {
      parts.push(`Question set on ${metadata.topic}.`);
    }
  }

  const methodologyBits: string[] = [];
  if (metadata.generator_names?.length) {
    methodologyBits.push(`generated by ${humanJoin(metadata.generator_names)}`);
  }
  if (metadata.editor_name) {
    methodologyBits.push(`edited by ${metadata.editor_name}`);
  }
  if (metadata.reviewer_names?.length) {
    methodologyBits.push(`reviewed by ${humanJoin(metadata.reviewer_names)}`);
  }
  if (methodologyBits.length > 0) {
    parts.push(`${methodologyBits.join(', ')}.`);
  }

  const configurationBits: string[] = [];
  if (metadata.difficulty) {
    configurationBits.push(`${metadata.difficulty} difficulty`);
  }
  configurationBits.push(formatCriteriaDepthLabel(metadata.criteria_depth));
  configurationBits.push(metadata.generate_answers === false ? 'no reference answers' : 'reference answers included');
  if (metadata.coverage_mode && metadata.coverage_mode !== 'none') {
    configurationBits.push(formatCoverageModeLabel(metadata.coverage_mode));
  }
  if (configurationBits.length > 0) {
    parts.push(`Configuration: ${configurationBits.join(', ')}.`);
  }

  return parts.join(' ');
}

function getSuiteCardDescription(suite: PromptSuite): string | null {
  // The backend now writes a human-readable description at generation time
  // (see build_friendly_suite_description in suite_pipeline.py), so the stored
  // value is authoritative. We only fall back to a metadata-derived summary if
  // the description is genuinely empty (legacy rows with nothing saved).
  const stored = suite.description?.trim();
  if (stored) {
    return stored;
  }
  const metadata = suite.generation_metadata;
  if (!metadata?.topic) {
    return null;
  }
  return buildFriendlyGeneratedSuiteDescription(suite, metadata);
}

