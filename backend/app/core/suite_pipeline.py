    async def _generate_batch(
        self,
        assignment: GenerationBatchAssignment,
    ) -> list[dict]:
        """Call the generator LLM and return parsed question list."""
        preset = assignment.preset
        count = assignment.question_count
        batch_index = assignment.generator_batch_index
        total_batches = assignment.generator_total_batches
        progress_detail = f"Generating batch {batch_index}/{total_batches} ({count} questions) — {preset.name}"
        started_at = await self._mark_generation_batch_started(
            task_id=assignment.task_id,
            preset_name=preset.name,
            global_batch_index=assignment.global_batch_index,
            total_global_batches=assignment.total_global_batches,
            generator_index=assignment.generator_index,
            total_generators=assignment.total_generators,
            generator_batch_index=assignment.generator_batch_index,
            generator_total_batches=assignment.generator_total_batches,
            question_count=assignment.question_count,
            detail=progress_detail,
        )
        await self._emit_log(
            "info",
            f"▶ {progress_detail}",
        )

        user_prompt = GENERATOR_USER_TEMPLATE.format(
            topic=self.topic,
            count=count,
            difficulty=self.config.difficulty,
            categories_instruction=self._categories_instruction(),
            generate_answers_instruction=self._answers_instruction(),
            criteria_depth_instruction=self._criteria_depth_instruction(),
            context_instruction=self._context_instruction_text(),
        )

        async def do_generate() -> list[dict]:
            result = await generate(
                preset,
                GENERATOR_SYSTEM_PROMPT,
                user_prompt,
                temperature=0.8,
                json_mode=False,
            )
            if not result.get("success"):
                raise PipelineError(
                    f"Generator failed (batch {batch_index}): {result.get('error', 'unknown error')}"
                )
            return await self._parse_json_list(
                result["content"],
                retry_context=f"generate batch {batch_index}",
                retry_system=GENERATOR_SYSTEM_PROMPT,
                retry_user=user_prompt,
                retry_preset=preset,
            )

        try:
            questions = await self._call_with_retry(
                do_generate,
                context=f"Generate batch {batch_index}/{total_batches} — {preset.name}",
            )
        except Exception:
            await self._mark_generation_batch_failed(
                task_id=assignment.task_id,
                preset_name=preset.name,
                global_batch_index=assignment.global_batch_index,
                detail=progress_detail,
            )
            raise

        await self._mark_generation_batch_finished(
            task_id=assignment.task_id,
            preset_name=preset.name,
            global_batch_index=assignment.global_batch_index,
            questions_generated=len(questions),
            detail=progress_detail,
        )
        elapsed = int(time.time() - started_at)
        await self._emit_log(
            "info",
            f"✓ Generate batch {batch_index}/{total_batches} complete — {len(questions)} questions in {self._format_elapsed(elapsed)}",
        )

        return questions

    async def _generate_coverage_batch(
        self,
        assignment: GenerationBatchAssignment,
    ) -> list[dict]:
        """Call the generator LLM for a coverage-assigned batch and annotate results."""
        preset = assignment.preset
        slots = assignment.slots or []
        batch_index = assignment.generator_batch_index
        total_batches = assignment.generator_total_batches
        progress_detail = (
            f"Generating coverage batch {batch_index}/{total_batches} ({len(slots)} assignments) — {preset.name}"
        )
        started_at = await self._mark_generation_batch_started(
            task_id=assignment.task_id,
            preset_name=preset.name,
            global_batch_index=assignment.global_batch_index,
            total_global_batches=assignment.total_global_batches,
            generator_index=assignment.generator_index,
            total_generators=assignment.total_generators,
            generator_batch_index=assignment.generator_batch_index,
            generator_total_batches=assignment.generator_total_batches,
            question_count=assignment.question_count,
            detail=progress_detail,
        )
        await self._emit_log(
            "info",
            f"▶ Coverage batch {batch_index}/{total_batches} ({len(slots)} assignments) — {preset.name}",
        )

        coverage_assignments = [self._coverage_slot_payload(slot) for slot in slots]
        user_prompt = COVERAGE_GENERATOR_USER_TEMPLATE.format(
            topic=self.topic,
            count=len(slots),
            difficulty=self.config.difficulty,
            coverage_mode=self.coverage_mode,
            categories_instruction=self._categories_instruction(),
            generate_answers_instruction=self._answers_instruction(),
            criteria_depth_instruction=self._criteria_depth_instruction(),
            context_instruction=self._context_instruction_text(use_digest=True),
            coverage_assignments_json=json.dumps(coverage_assignments, indent=2),
        )

        async def do_generate() -> list[dict]:
            result = await generate(
                preset,
                GENERATOR_SYSTEM_PROMPT,
                user_prompt,
                temperature=0.8,
                json_mode=False,
            )
            if not result.get("success"):
                raise PipelineError(
                    f"Coverage generator failed (batch {batch_index}): {result.get('error', 'unknown error')}"
                )
            return await self._parse_json_list(
                result["content"],
                retry_context=f"coverage generate batch {batch_index}",
                retry_system=GENERATOR_SYSTEM_PROMPT,
                retry_user=user_prompt,
                retry_preset=preset,
            )

        try:
            questions = await self._call_with_retry(
                do_generate,
                context=f"Coverage generate batch {batch_index}/{total_batches} — {preset.name}",
            )
        except Exception:
            await self._mark_generation_batch_failed(
                task_id=assignment.task_id,
                preset_name=preset.name,
                global_batch_index=assignment.global_batch_index,
                detail=progress_detail,
            )
            raise

        if len(questions) != len(slots):
            await self._mark_generation_batch_failed(
                task_id=assignment.task_id,
                preset_name=preset.name,
                global_batch_index=assignment.global_batch_index,
                detail=progress_detail,
            )
            raise PipelineError(
                f"Coverage batch {batch_index} returned {len(questions)} questions for {len(slots)} assignments"
            )

        await self._mark_generation_batch_finished(
            task_id=assignment.task_id,
            preset_name=preset.name,
            global_batch_index=assignment.global_batch_index,
            questions_generated=len(questions),
            detail=progress_detail,
        )
        elapsed = int(time.time() - started_at)
        await self._emit_log(
            "info",
            f"✓ Coverage batch {batch_index}/{total_batches} complete — {len(questions)} questions in {self._format_elapsed(elapsed)}",
        )
        return questions

