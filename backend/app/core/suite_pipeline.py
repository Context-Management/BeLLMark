    def _annotate_generated_batch_entries(
        self,
        assignment: GenerationBatchAssignment,
        batch_questions: list[dict],
    ) -> list[tuple[int, dict]]:
        final_order_indices = assignment.final_order_indices or []
        if len(final_order_indices) != len(batch_questions):
            raise PipelineError(
                f"Generation batch {assignment.task_id} produced {len(batch_questions)} questions "
                f"for {len(final_order_indices)} final-order slots"
            )

        entries: list[tuple[int, dict]] = []
        for item_index, (final_order_index, question) in enumerate(zip(final_order_indices, batch_questions, strict=True)):
            annotated = dict(question)
            if assignment.slots is not None:
                slot = assignment.slots[item_index]
                annotated["generation_slot_index"] = slot.slot_index
                annotated["coverage_topic_ids"] = list(slot.required_leaf_ids)
                annotated["coverage_topic_labels"] = list(slot.required_leaf_labels)
            entries.append((int(final_order_index), annotated))
        return entries

    async def _run_generation_assignments_streaming(
        self,
        assignments: list[GenerationBatchAssignment],
        *,
        on_completed,
        coverage_enabled: bool,
        restored_completed_count: int = 0,
        total_batches: int | None = None,
    ) -> list[tuple[GenerationBatchAssignment, list[dict]]]:
        self._reset_generation_batch_tracking(total_batches or (len(assignments) + restored_completed_count))
        self._completed_generation_batches = restored_completed_count
        if not assignments:
            return []

        semaphore = asyncio.Semaphore(max(self.config.generation_concurrency, 1))
        completed_results: list[tuple[GenerationBatchAssignment, list[dict]]] = []

        async def run_one(
            assignment: GenerationBatchAssignment,
        ) -> tuple[GenerationBatchAssignment, list[dict]]:
            async with semaphore:
                self._raise_if_cancelled()
                if assignment.slots is not None:
                    batch_questions = await self._generate_coverage_batch(assignment)
                else:
                    batch_questions = await self._generate_batch(assignment)
                return assignment, batch_questions

        tasks = [asyncio.create_task(run_one(assignment)) for assignment in assignments]
        try:
            for future in asyncio.as_completed(tasks):
                result = await future
                await self._record_generation_checkpoint(result[0], result[1])
                completed_results.append(result)
                maybe_result = on_completed(*result)
                if asyncio.iscoroutine(maybe_result):
                    await maybe_result
        except Exception as error:
            for task in tasks:
                if not task.done():
                    task.cancel()
            await asyncio.gather(*tasks, return_exceptions=True)
            setattr(error, "completed_generation_results", completed_results)
            setattr(
                error,
                "partial_questions",
                self._assemble_generated_questions(completed_results, coverage_enabled=coverage_enabled),
            )
            raise

        return completed_results

    async def _review_batch(
        self,
        batch_questions: list[dict],
        batch_index: int,
        total_batches: int,
    ) -> ReviewBatchOutcome:
        task_id = f"review-batch-{batch_index}"
        started_at = time.time()
        reviewers_status = {
            reviewer.name: {"model": reviewer.name, "status": "pending"}
            for reviewer in self.reviewer_presets
        }

        async with self._review_progress_lock:
            self._review_batches_in_flight[task_id] = {
                "task_id": task_id,
                "batch_index": batch_index,
                "total_batches": total_batches,
                "started_at": started_at,
                "detail": f"Review batch {batch_index}/{total_batches}",
                "reviewers_status": reviewers_status,
            }
            self._refresh_review_display_state()
            await self._emit_progress(phase="review", detail=self._review_progress_detail())

        async def review_one(reviewer: ModelPreset) -> list[dict] | None:
            reviewer_started_at = time.time()
            async with self._review_progress_lock:
                batch_state = self._review_batches_in_flight.get(task_id)
                if batch_state is not None:
                    batch_state["reviewers_status"][reviewer.name] = {
                        "model": reviewer.name,
                        "status": "working",
                        "started_at": int(reviewer_started_at),
                    }
                    self._refresh_review_display_state()
                    await self._emit_progress(phase="review", detail=self._review_progress_detail())
            await self._emit_log(
                "info",
                f"▶ Review batch {batch_index}/{total_batches} — {reviewer.name}",
            )

            async def update_retry_status(attempt: int, _error: Exception) -> None:
                async with self._review_progress_lock:
                    batch_state = self._review_batches_in_flight.get(task_id)
                    if batch_state is not None:
                        batch_state["reviewers_status"][reviewer.name] = {
                            "model": reviewer.name,
                            "status": "retry",
                            "started_at": int(reviewer_started_at),
                        }
                        self._refresh_review_display_state()
                        await self._emit_progress(phase="review", detail=self._review_progress_detail())

            async def update_exhausted_status(_error: Exception | None) -> None:
                elapsed = int(time.time() - reviewer_started_at)
                async with self._review_progress_lock:
                    batch_state = self._review_batches_in_flight.get(task_id)
                    if batch_state is not None:
                        batch_state["reviewers_status"][reviewer.name] = {
                            "model": reviewer.name,
                            "status": "skipped",
                            "elapsed_seconds": elapsed,
                        }
                        self._refresh_review_display_state()
                        await self._emit_progress(phase="review", detail=self._review_progress_detail())

            reviews = await self._call_with_retry(
                lambda: self._call_reviewer(batch_questions, reviewer, batch_index, total_batches),
                context=f"Review batch {batch_index}/{total_batches} — {reviewer.name}",
                on_exhausted="skip",
                on_retry=update_retry_status,
                on_exhausted_callback=update_exhausted_status,
            )
            if reviews is None:
                return None

            elapsed = int(time.time() - reviewer_started_at)
            async with self._review_progress_lock:
                batch_state = self._review_batches_in_flight.get(task_id)
                if batch_state is not None:
                    batch_state["reviewers_status"][reviewer.name] = {
                        "model": reviewer.name,
                        "status": "done",
                        "elapsed_seconds": elapsed,
                    }
                    self._refresh_review_display_state()
                    await self._emit_progress(phase="review", detail=self._review_progress_detail())
            await self._emit_log(
                "info",
                f"✓ Review batch {batch_index}/{total_batches} — {reviewer.name} done in {self._format_elapsed(elapsed)}",
            )
            return reviews

        review_results = await asyncio.gather(*[
            review_one(reviewer)
            for reviewer in self.reviewer_presets
        ])
        successful_reviews = [review for review in review_results if review is not None]

        async with self._review_progress_lock:
            batch_state = self._review_batches_in_flight.pop(task_id, None)
            self._questions_reviewed += len(batch_questions)
            self._refresh_review_display_state()
            await self._emit_progress(phase="review", detail=self._review_progress_detail())

        reviewer_rows = list((batch_state or {"reviewers_status": reviewers_status})["reviewers_status"].values())
        skipped_reviewers = [
            status for status in reviewer_rows
            if status.get("status") == "skipped"
        ]
        self._batch_review_coverage.append({
            "batch": batch_index,
            "required_reviewers": [r.name for r in self.reviewer_presets],
            "successful_reviewers": [
                status["model"] for status in reviewer_rows
                if status.get("status") == "done"
            ],
            "skipped_reviewers": skipped_reviewers,
        })

        return ReviewBatchOutcome(
            task_id=task_id,
            batch_index=batch_index,
            total_batches=total_batches,
            questions=batch_questions,
            successful_reviews=successful_reviews,
            required_reviewers=[r.name for r in self.reviewer_presets],
            skipped_reviewers=skipped_reviewers,
        )

    async def _generate_and_review_across_generators(
        self,
        generation_plan: list[tuple[ModelPreset, int]],
        *,
        coverage_slots: list[Any] | None = None,
    ) -> list[ReviewBatchOutcome]:
        coverage_enabled = coverage_slots is not None
        assignments = self._build_generation_batch_assignments(
            generation_plan,
            coverage_slots=coverage_slots,
        )
        if not assignments:
            return []

        restored_generation_entries = {
            entry["task_id"]: list(entry.get("questions") or [])
            for entry in self._job_checkpoint.get("generation_results") or []
        }
        restored_review_outcomes = {
            int(entry["batch_index"]): ReviewBatchOutcome(
                task_id=entry["task_id"],
                batch_index=int(entry["batch_index"]),
                total_batches=int(entry["total_batches"]),
                questions=list(entry.get("questions") or []),
                successful_reviews=list(entry.get("successful_reviews") or []),
                required_reviewers=list(entry.get("required_reviewers") or []),
                skipped_reviewers=list(entry.get("skipped_reviewers") or []),
            )
            for entry in self._job_checkpoint.get("review_outcomes") or []
        }
        restored_results: list[tuple[GenerationBatchAssignment, list[dict]]] = []
        pending_assignments: list[GenerationBatchAssignment] = []
        for assignment in assignments:
            restored_questions = restored_generation_entries.get(assignment.task_id)
            if restored_questions is None:
                pending_assignments.append(assignment)
            else:
                restored_results.append((assignment, restored_questions))

        self._model = assignments[0].preset.name
        detail = (
            f"Generating and reviewing coverage across {len(generation_plan)} generators"
            if coverage_enabled
            else f"Generating and reviewing across {len(generation_plan)} generators"
        )
        await self._emit_progress(phase="generate", detail=detail)
        await self._emit_log(
            "info",
            f"▶ Generation plan ready — {len(assignments)} batches across {len(generation_plan)} generators "
            f"(generation concurrency {max(self.config.generation_concurrency, 1)}, "
            f"review concurrency {max(self.config.review_batch_concurrency, 1)})",
        )

        for generator_index, (preset, generator_count) in enumerate(generation_plan, start=1):
            generator_batches = sum(
                1
                for assignment in assignments
                if assignment.generator_index == generator_index
            )
            unit = "assignments" if coverage_enabled else "prompts"
            await self._emit_log(
                "info",
                f"▶ Scheduled generator {generator_index}/{len(generation_plan)} — {preset.name} "
                f"({generator_count} {unit} across {generator_batches} batches)",
            )

        ready_questions: dict[int, dict] = {}
        next_review_start_index = 0
        total_review_batches = len(self._split_batches(self.count))
        review_tasks: list[asyncio.Task[ReviewBatchOutcome]] = []
        review_semaphore = asyncio.Semaphore(max(self.config.review_batch_concurrency, 1))

        async def run_review_task(
            batch_index: int,
            batch_questions: list[dict],
        ) -> ReviewBatchOutcome:
            async with review_semaphore:
                self._raise_if_cancelled()
                outcome = await self._review_batch(batch_questions, batch_index, total_review_batches)
                await self._record_review_checkpoint(outcome)
                return outcome

        async def schedule_ready_review_batches() -> None:
            nonlocal next_review_start_index
            while next_review_start_index < self.count:
                batch_size = min(_BATCH_SIZE, self.count - next_review_start_index)
                batch_indices = list(range(next_review_start_index, next_review_start_index + batch_size))
                if any(index not in ready_questions for index in batch_indices):
                    return
                batch_questions = [ready_questions.pop(index) for index in batch_indices]
                batch_index = (next_review_start_index // _BATCH_SIZE) + 1
                if batch_index not in restored_review_outcomes:
                    review_tasks.append(asyncio.create_task(run_review_task(batch_index, batch_questions)))
                next_review_start_index += batch_size

        async def on_generation_completed(
            assignment: GenerationBatchAssignment,
            batch_questions: list[dict],
        ) -> None:
            for final_order_index, question in self._annotate_generated_batch_entries(assignment, batch_questions):
                ready_questions[final_order_index] = question
            await schedule_ready_review_batches()

        completed_generation_results: list[tuple[GenerationBatchAssignment, list[dict]]] = []
        try:
            for restored_assignment, restored_questions in restored_results:
                await on_generation_completed(restored_assignment, restored_questions)
            completed_generation_results = await self._run_generation_assignments_streaming(
                pending_assignments,
                on_completed=on_generation_completed,
                coverage_enabled=coverage_enabled,
                restored_completed_count=len(restored_results),
                total_batches=len(assignments),
            )
        except Exception:
            for task in review_tasks:
                task.cancel()
            await asyncio.gather(*review_tasks, return_exceptions=True)
            raise

        completed_generation_results = restored_results + completed_generation_results
        await schedule_ready_review_batches()
        if next_review_start_index != self.count:
            raise PipelineError("review scheduling stalled before all generated questions were queued")

        review_results = await asyncio.gather(*review_tasks, return_exceptions=True)
        completed_outcomes = list(restored_review_outcomes.values()) + [
            result
            for result in review_results
            if isinstance(result, ReviewBatchOutcome)
        ]
        failures = [result for result in review_results if isinstance(result, Exception)]
        if failures:
            error = failures[0]
            partial_questions = [
                question
                for outcome in sorted(completed_outcomes, key=lambda item: item.batch_index)
                for question in outcome.questions
            ]
            if not partial_questions:
                partial_questions = self._assemble_generated_questions(
                    completed_generation_results,
                    coverage_enabled=coverage_enabled,
                )
            setattr(error, "partial_questions", partial_questions)
            raise error

        return completed_outcomes

    async def _merge_review_outcomes(
        self,
        review_outcomes: list[ReviewBatchOutcome],
    ) -> list[dict]:
        if not review_outcomes:
            return []

        merged_batches_by_index = {
            int(entry["batch_index"]): list(entry.get("questions") or [])
            for entry in self._job_checkpoint.get("merged_batches") or []
        }
        merged_questions_by_index = {
            batch_index: list(questions)
            for batch_index, questions in merged_batches_by_index.items()
        }
        total_batches = len(review_outcomes)
        self._total_batches = total_batches
        semaphore = asyncio.Semaphore(max(self.config.review_batch_concurrency, 1))

        async def run_one(outcome: ReviewBatchOutcome) -> tuple[int, list[dict]]:
            async with semaphore:
                self._raise_if_cancelled()
                batch_index = outcome.batch_index
                batch_questions = outcome.questions
                self._batch = batch_index
                if outcome.skipped_reviewers:
                    await self._emit_log(
                        "warning",
                        f"⚠ {len(outcome.skipped_reviewers)} reviewer(s) skipped for batch {batch_index}",
                    )
                if outcome.successful_reviews:
                    await self._emit_log(
                        "info",
                        f"✓ Reviews batch {batch_index}/{total_batches} complete — "
                        f"{len(outcome.successful_reviews)}/{len(self.reviewer_presets)} reviewers succeeded",
                    )
                    batch_questions = await self._merge_batch(
                        batch_questions,
                        outcome.successful_reviews,
                        batch_index,
                        total_batches,
                    )
                else:
                    await self._emit_log(
                        "warning",
                        f"⚠ All reviewers failed for batch {batch_index}/{total_batches} — using raw generated questions",
                    )
                return batch_index, batch_questions

        pending_outcomes = [
            outcome
            for outcome in sorted(review_outcomes, key=lambda item: item.batch_index)
            if outcome.batch_index not in merged_batches_by_index
        ]
        tasks = [asyncio.create_task(run_one(outcome)) for outcome in pending_outcomes]
        try:
            for future in asyncio.as_completed(tasks):
                batch_index, batch_questions = await future
                self._questions_merged += len(batch_questions)
                merged_questions_by_index[batch_index] = list(batch_questions)
                await self._record_merged_batch(batch_index, batch_questions)
        except Exception as error:
            for task in tasks:
                if not task.done():
                    task.cancel()
            await asyncio.gather(*tasks, return_exceptions=True)
            setattr(
                error,
                "partial_questions",
                [
                    question
                    for batch_index in sorted(merged_questions_by_index)
                    for question in merged_questions_by_index[batch_index]
                ],
            )
            raise

        return [
            question
            for batch_index in sorted(merged_questions_by_index)
            for question in merged_questions_by_index[batch_index]
        ]

    async def _generate_across_generators(
        self,
        generation_plan: list[tuple[ModelPreset, int]],
        *,
        coverage_slots: list[Any] | None = None,
    ) -> list[dict]:
        coverage_enabled = coverage_slots is not None
        assignments = self._build_generation_batch_assignments(
            generation_plan,
            coverage_slots=coverage_slots,
        )
        if not assignments:
            return []

        restored_generation_entries = {
            entry["task_id"]: list(entry.get("questions") or [])
            for entry in self._job_checkpoint.get("generation_results") or []
        }
        restored_results: list[tuple[GenerationBatchAssignment, list[dict]]] = []
        pending_assignments: list[GenerationBatchAssignment] = []
        for assignment in assignments:
            restored_questions = restored_generation_entries.get(assignment.task_id)
            if restored_questions is None:
                pending_assignments.append(assignment)
            else:
                restored_results.append((assignment, restored_questions))

        self._model = assignments[0].preset.name
        detail = (
            f"Generating coverage slots across {len(generation_plan)} generators"
            if coverage_enabled
            else f"Generating questions across {len(generation_plan)} generators"
        )
        await self._emit_progress(phase="generate", detail=detail)
        await self._emit_log(
            "info",
            f"▶ Generation plan ready — {len(assignments)} batches across {len(generation_plan)} generators "
            f"(concurrency {max(self.config.generation_concurrency, 1)})",
        )

        for generator_index, (preset, generator_count) in enumerate(generation_plan, start=1):
            generator_batches = sum(
                1
                for assignment in assignments
                if assignment.generator_index == generator_index
            )
            unit = "assignments" if coverage_enabled else "prompts"
            await self._emit_log(
                "info",
                f"▶ Scheduled generator {generator_index}/{len(generation_plan)} — {preset.name} "
                f"({generator_count} {unit} across {generator_batches} batches)",
            )

        completed_results = await self._run_generation_assignments(
            pending_assignments,
            coverage_enabled=coverage_enabled,
            restored_completed_count=len(restored_results),
            total_batches=len(assignments),
        )
        completed_results = restored_results + completed_results
        return self._assemble_generated_questions(
            completed_results,
            coverage_enabled=coverage_enabled,
        )

