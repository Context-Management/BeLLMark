    async def _call_with_retry(
        self,
        call_fn,
        context: str,
        max_retries: int = 2,
        on_exhausted: str = "raise",
        on_retry=None,
        on_exhausted_callback=None,
    ) -> Any:
        """Retry wrapper for transport and JSON parse failures."""
        delays = [5, 15]
        last_error: Exception | None = None
        for attempt in range(max_retries + 1):
            try:
                if attempt > 0:
                    delay = delays[min(attempt - 1, len(delays) - 1)]
                    await self._emit_log(
                        "warning",
                        f"⟳ {context} retry {attempt}/{max_retries} ({delay}s delay)",
                    )
                    await asyncio.sleep(delay)
                return await call_fn()
            except Exception as error:
                last_error = error
                if attempt < max_retries:
                    if on_retry:
                        maybe_result = on_retry(attempt + 1, error)
                        if asyncio.iscoroutine(maybe_result):
                            await maybe_result
                    await self._emit_log("warning", f"⚠ {context} failed: {error}")
                else:
                    await self._emit_log("error", f"✗ {context} failed after {max_retries} retries: {error}")

        if on_exhausted_callback:
            maybe_result = on_exhausted_callback(last_error)
            if asyncio.iscoroutine(maybe_result):
                await maybe_result

        if on_exhausted == "skip":
            await self._emit_log(
                "warning",
                f"⚠ {context} skipped after {max_retries} failed retries",
            )
            return None
        if on_exhausted == "default_rubric":
            await self._emit_log(
                "warning",
                f"⚠ {context} failed after retries — using default rubric",
            )
            return self._default_rubric()
        if last_error is not None:
            raise last_error
        raise PipelineError(f"{context} failed without an error")

