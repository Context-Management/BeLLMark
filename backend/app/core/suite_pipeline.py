    async def _merge_batch(
        self,
        questions: list[dict],
        all_reviews: list[list[dict]],
        batch_index: int,
        total_batches: int,
    ) -> list[dict]:
        """Call generator to merge original questions with all reviewer inputs."""
        task_id = f"merge-batch-{batch_index}"
        progress_detail = f"Merging batch {batch_index}/{total_batches}"
        started_at = await self._mark_merge_batch_started(
            task_id=task_id,
            batch_index=batch_index,
            total_batches=total_batches,
            detail=progress_detail,
        )
        await self._emit_log(
            "info",
            f"▶ Merge batch {batch_index}/{total_batches} — {self.editor_preset.name}",
        )

        user_prompt = MERGE_USER_TEMPLATE.format(
            count=len(questions),
            context_instruction=self._context_instruction_text(),
            questions_json=json.dumps(questions, indent=2),
            reviews_json=json.dumps(all_reviews, indent=2),
        )

        async def do_merge() -> list[dict]:
            result = await generate(
                self.editor_preset,
                MERGE_SYSTEM_PROMPT,
                user_prompt,
                temperature=0.5,
                json_mode=False,
            )
            if not result.get("success"):
                raise PipelineError(
                    f"Merge failed (batch {batch_index}): {result.get('error', 'unknown error')}"
                )
            return await self._parse_json_list(
                result["content"],
                retry_context=f"merge batch {batch_index}",
                retry_system=MERGE_SYSTEM_PROMPT,
                retry_user=user_prompt,
                retry_preset=self.editor_preset,
            )

        try:
            merged = await self._call_with_retry(
                do_merge,
                context=f"Merge batch {batch_index}/{total_batches} — {self.editor_preset.name}",
            )
            if len(merged) != len(questions):
                raise PipelineError(
                    f"Merge batch {batch_index} returned {len(merged)} questions for {len(questions)} originals"
                )

            preserved_merged: list[dict] = []
            for original, merged_question in zip(questions, merged):
                preserved = dict(merged_question)
                if "generation_slot_index" in original:
                    preserved["generation_slot_index"] = original.get("generation_slot_index")
                if "coverage_topic_ids" in original:
                    preserved["coverage_topic_ids"] = list(original.get("coverage_topic_ids") or [])
                if "coverage_topic_labels" in original:
                    preserved["coverage_topic_labels"] = list(original.get("coverage_topic_labels") or [])
                preserved_merged.append(preserved)
        except Exception:
            await self._mark_merge_batch_failed(
                task_id=task_id,
                batch_index=batch_index,
            )
            raise

        await self._mark_merge_batch_finished(
            task_id=task_id,
            batch_index=batch_index,
        )
        elapsed = int(time.time() - started_at)
        await self._emit_log(
            "info",
            f"✓ Merge batch {batch_index}/{total_batches} complete — {len(preserved_merged)} questions in {self._format_elapsed(elapsed)}",
        )
        return preserved_merged

