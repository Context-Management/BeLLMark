    def _batch_sizes(self) -> list[int]:
        """Split self.count into batches of at most _BATCH_SIZE."""
        return self._split_batches(self.count)

    def _split_batches(self, total: int, batch_size: int = _BATCH_SIZE) -> list[int]:
        """Split a total count into batches of at most _BATCH_SIZE."""
        if total <= 0:
            return []
        n_batches = ceil(total / batch_size)
        sizes = []
        remaining = total
        for _ in range(n_batches):
            sizes.append(min(batch_size, remaining))
            remaining -= batch_size
        return sizes

