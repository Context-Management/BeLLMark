    def _refresh_generation_display_state(self, fallback_model: str | None = None) -> None:
        if self._generation_calls_in_flight:
            active_call = min(
                self._generation_calls_in_flight.values(),
                key=lambda item: (item["started_at"], item["global_batch_index"]),
            )
            self._batch = active_call["global_batch_index"]
            self._call_started_at = active_call["started_at"]
            self._model = active_call["model"]
            return

        self._call_started_at = None
        if fallback_model is not None:
            self._model = fallback_model

    def _build_generation_batch_assignments(
        self,
        generation_plan: list[tuple[ModelPreset, int]],
        *,
        coverage_slots: list[Any] | None = None,
    ) -> list[GenerationBatchAssignment]:
        per_generator_assignments: list[list[GenerationBatchAssignment]] = []
        total_generators = len(generation_plan)
        per_generator_slot_buckets: list[list[Any]] | None = None
        per_generator_final_indices: list[list[int]] = [[] for _ in generation_plan]

        if coverage_slots is not None:
            per_generator_slot_buckets = [[] for _ in generation_plan]
            remaining_counts = [generator_count for _, generator_count in generation_plan]
            cursor = 0
            for slot in coverage_slots:
                checked = 0
                while checked < total_generators and remaining_counts[cursor] == 0:
                    cursor = (cursor + 1) % total_generators
                    checked += 1
                if remaining_counts[cursor] == 0:
                    raise PipelineError("coverage slot allocation exhausted generator capacity")
                per_generator_slot_buckets[cursor].append(slot)
                per_generator_final_indices[cursor].append(slot.slot_index)
                remaining_counts[cursor] -= 1
                cursor = (cursor + 1) % total_generators
        else:
            remaining_counts = [generator_count for _, generator_count in generation_plan]
            cursor = 0
            for final_order_index in range(sum(remaining_counts)):
                checked = 0
                while checked < total_generators and remaining_counts[cursor] == 0:
                    cursor = (cursor + 1) % total_generators
                    checked += 1
                if remaining_counts[cursor] == 0:
                    raise PipelineError("generation order allocation exhausted generator capacity")
                per_generator_final_indices[cursor].append(final_order_index)
                remaining_counts[cursor] -= 1
                cursor = (cursor + 1) % total_generators

        for generator_index, (preset, generator_count) in enumerate(generation_plan, start=1):
            assigned_slots = (
                per_generator_slot_buckets[generator_index - 1]
                if per_generator_slot_buckets is not None
                else None
            )
            assigned_final_indices = per_generator_final_indices[generator_index - 1]
            if assigned_slots is not None:
                batch_sizes = self._split_batches(
                    len(assigned_slots),
                    batch_size=_COVERAGE_GENERATION_BATCH_SIZE,
                )
            else:
                batch_sizes = self._split_batches(generator_count)

            generator_total_batches = len(batch_sizes)
            offset = 0
            generator_assignments: list[GenerationBatchAssignment] = []
            for generator_batch_index, batch_count in enumerate(batch_sizes, start=1):
                batch_slots = None
                question_count = batch_count
                if assigned_slots is not None:
                    batch_slots = assigned_slots[offset : offset + batch_count]
                    offset += batch_count
                    question_count = len(batch_slots)
                batch_final_indices = assigned_final_indices[offset - question_count : offset] if assigned_slots is not None else assigned_final_indices[offset : offset + batch_count]
                if assigned_slots is None:
                    offset += batch_count

                generator_assignments.append(GenerationBatchAssignment(
                    task_id=(
                        f"generator-{generator_index}-batch-{generator_batch_index}"
                    ),
                    preset=preset,
                    generator_index=generator_index,
                    total_generators=total_generators,
                    generator_batch_index=generator_batch_index,
                    generator_total_batches=generator_total_batches,
                    global_batch_index=0,
                    total_global_batches=0,
                    question_count=question_count,
                    slots=batch_slots,
                    final_order_indices=list(batch_final_indices),
                ))
            per_generator_assignments.append(generator_assignments)

        assignments: list[GenerationBatchAssignment] = []
        while any(per_generator_assignments):
            for queue in per_generator_assignments:
                if queue:
                    assignments.append(queue.pop(0))

        total_global_batches = len(assignments)
        for global_batch_index, assignment in enumerate(assignments, start=1):
            assignment.task_id = f"{assignment.task_id}-global-{global_batch_index}"
            assignment.global_batch_index = global_batch_index
            assignment.total_global_batches = total_global_batches

        return assignments

    def _assemble_generated_questions(
        self,
        completed_results: list[tuple[GenerationBatchAssignment, list[dict]]],
        *,
        coverage_enabled: bool,
    ) -> list[dict]:
        sorted_results = sorted(
            completed_results,
            key=lambda item: item[0].global_batch_index,
        )
        if coverage_enabled:
            questions: list[dict] = []
            for assignment, batch_questions in sorted_results:
                for slot, question in zip(assignment.slots or [], batch_questions):
                    annotated = dict(question)
                    annotated["generation_slot_index"] = slot.slot_index
                    annotated["coverage_topic_ids"] = list(slot.required_leaf_ids)
                    annotated["coverage_topic_labels"] = list(slot.required_leaf_labels)
                    questions.append(annotated)
            questions.sort(key=lambda question: int(question["generation_slot_index"]))
            return questions

        generator_questions: dict[int, list[dict]] = {}
        generator_presets: dict[int, ModelPreset] = {}
        for assignment, batch_questions in sorted_results:
            generator_presets[assignment.generator_index] = assignment.preset
            generator_questions.setdefault(assignment.generator_index, []).extend(batch_questions)

        per_generator = [
            (generator_presets[index], generator_questions.get(index, []))
            for index in sorted(generator_presets)
        ]
        return interleave_generated_questions(per_generator)

    async def _run_generation_assignments(
        self,
        assignments: list[GenerationBatchAssignment],
        *,
        coverage_enabled: bool,
        restored_completed_count: int = 0,
        total_batches: int | None = None,
    ) -> list[tuple[GenerationBatchAssignment, list[dict]]]:
        self._reset_generation_batch_tracking(total_batches or (len(assignments) + restored_completed_count))
        self._completed_generation_batches = restored_completed_count
        if not assignments:
            return []

        semaphore = asyncio.Semaphore(max(self.config.generation_concurrency, 1))

        async def run_one(
            assignment: GenerationBatchAssignment,
        ) -> tuple[GenerationBatchAssignment, list[dict]]:
            async with semaphore:
                self._raise_if_cancelled()
                if assignment.slots is not None:
                    batch_questions = await self._generate_coverage_batch(assignment)
                else:
                    batch_questions = await self._generate_batch(assignment)
                await self._record_generation_checkpoint(assignment, batch_questions)
                return assignment, batch_questions

        results = await asyncio.gather(*[
            run_one(assignment)
            for assignment in assignments
        ], return_exceptions=True)

        completed_results = [
            result
            for result in results
            if isinstance(result, tuple)
        ]
        failures = [result for result in results if isinstance(result, Exception)]
        if failures:
            error = failures[0]
            setattr(error, "completed_generation_results", completed_results)
            setattr(
                error,
                "partial_questions",
                self._assemble_generated_questions(completed_results, coverage_enabled=coverage_enabled),
            )
            raise error
        return completed_results

