    async def _run_pipeline(self) -> PromptSuite:
        self._raise_if_cancelled()
        reviewer_names = ", ".join(r.name for r in self.reviewer_presets) if self.reviewer_presets else "none"
        generator_names = ", ".join(p.name for p in self.generator_presets)
        await self._emit_log(
            "info",
            f"Started pipeline — {self.count} questions, batch size {_BATCH_SIZE}, "
            f"generators: {generator_names}, editor: {self.editor_preset.name}, reviewers: {reviewer_names}",
        )

        # Load context attachment once if provided
        if self.context_attachment_id is not None:
            self._context_text = self._load_context_attachment()

        questions_to_save: list[dict] = []
        prepared_questions = self._job_checkpoint.get("prepared_questions")
        restored_rubric = self._job_checkpoint.get("rubric")

        try:
            generation_plan = build_generation_plan(self.count, self.generator_presets)
            if not generation_plan:
                raise PipelineError("No generator presets configured")

            coverage_enabled = self.coverage_mode != "none"
            if coverage_enabled:
                if not self.coverage_spec:
                    raise PipelineError("coverage_spec is required when coverage_mode is enabled")
                await self._emit_progress(
                    phase="plan",
                    detail="Planning coverage slots",
                )
                self._coverage_plan = build_coverage_plan(
                    self.coverage_spec,
                    count=self.count,
                    mode=self.coverage_mode,
                    max_topics_per_question=self.max_topics_per_question,
                )
                await self._emit_log(
                    "info",
                    f"▶ Coverage plan ready — {len(self._coverage_plan.slots)} slots across "
                    f"{len(self._coverage_plan.leaves)} required leaf topics",
                )
                if self._context_text:
                    self._context_digest = await self._build_context_digest(self._coverage_plan)
                    await self._emit_log(
                        "info",
                        f"▶ Coverage context digest ready ({len(self._context_digest)} chars)",
                    )

            if prepared_questions:
                questions_to_save = list(prepared_questions)
            else:
                coverage_slots = list(self._coverage_plan.slots) if coverage_enabled and self._coverage_plan else None
                if self.reviewer_presets:
                    try:
                        review_outcomes = await self._generate_and_review_across_generators(
                            generation_plan,
                            coverage_slots=coverage_slots,
                        )
                    except Exception as error:
                        partial_questions = getattr(error, "partial_questions", None)
                        if partial_questions:
                            questions_to_save.extend(partial_questions)
                        raise
                    questions_to_save = await self._merge_review_outcomes(review_outcomes)
                else:
                    try:
                        questions_to_save = await self._generate_across_generators(
                            generation_plan,
                            coverage_slots=coverage_slots,
                        )
                    except Exception as error:
                        partial_questions = getattr(error, "partial_questions", None)
                        if partial_questions:
                            questions_to_save.extend(partial_questions)
                        raise

                if coverage_enabled and questions_to_save:
                    self._coverage_validation_results = await self._validate_coverage_all(questions_to_save)
                    for question in questions_to_save:
                        slot_index = question.get("generation_slot_index")
                        if slot_index is None:
                            continue
                        validation = self._coverage_validation_results.get(int(slot_index))
                        if validation:
                            question["coverage_status"] = validation.get("coverage_status")
                    uncovered = [
                        result for result in self._coverage_validation_results.values()
                        if result.get("coverage_status") != "covered"
                    ]
                    if uncovered:
                        await self._emit_log(
                            "warning",
                            f"⚠ Coverage validation flagged {len(uncovered)} question(s) for repair",
                        )

                if questions_to_save:
                    questions_to_save = await self._dedupe_questions(questions_to_save)
                await self._record_prepared_questions(questions_to_save)

            acceptance_failures = self._acceptance_failures(questions_to_save)
            if acceptance_failures:
                raise PipelineError("; ".join(acceptance_failures))

            self._raise_if_cancelled()
            rubric = list(restored_rubric) if restored_rubric else await self._synthesize_rubric(questions_to_save)
            if not restored_rubric:
                await self._record_rubric_checkpoint(rubric)
            self._raise_if_cancelled()
            suite = await self._save_suite(self.name, questions_to_save, rubric)
            await self._persist_job_state(
                status=RunStatus.completed,
                phase="save",
                snapshot_payload=self.snapshot(),
                checkpoint_payload=self._job_checkpoint,
                suite_id=suite.id,
                error=None,
                completed=True,
            )
            total_elapsed = int(time.time() - self.started_at)
            await self._emit_log(
                "info",
                f"✓ Pipeline complete — {self.saved_count} questions in {self._format_elapsed(total_elapsed)}",
            )
            return suite
        except PipelinePausedForResume:
            await self._emit_log("info", "Pipeline paused for backend restart resume")
            await self._persist_job_state(
                status=RunStatus.running,
                phase=self._current_phase,
                snapshot_payload=self.snapshot(),
                checkpoint_payload=self._job_checkpoint,
            )
            raise
        except PipelineCancelledError:
            await self._emit_log("info", "Pipeline cancelled by user")
            await self._persist_job_state(
                status=RunStatus.cancelled,
                phase=self._current_phase,
                snapshot_payload=self.snapshot(),
                checkpoint_payload=self._job_checkpoint,
                error="Cancelled by user",
                completed=True,
            )
            raise
        except Exception as error:
            partial_questions = getattr(error, "partial_questions", questions_to_save)
            if partial_questions:
                rubric = self._default_rubric()
                suite = await self._save_suite(f"{self.name} [PARTIAL]", partial_questions, rubric)
                self._partial_suite = suite
                self._partial_error = str(error)
                await self._persist_job_state(
                    status=RunStatus.failed,
                    phase=self._current_phase,
                    snapshot_payload=self.snapshot(),
                    checkpoint_payload=self._job_checkpoint,
                    partial_suite_id=suite.id,
                    error=str(error),
                    completed=True,
                )
                await self._emit_log(
                    "warning",
                    f"⚠ Pipeline partially saved — {len(partial_questions)}/{self.count} questions",
                )
            else:
                await self._persist_job_state(
                    status=RunStatus.failed,
                    phase=self._current_phase,
                    snapshot_payload=self.snapshot(),
                    checkpoint_payload=self._job_checkpoint,
                    error=str(error),
                    completed=True,
                )
            raise

