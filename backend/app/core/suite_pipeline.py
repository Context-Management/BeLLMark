class SuitePipeline:
    """Async suite generation pipeline.

    Lifecycle:
        1. Construct and register in active_suite_pipelines.
        2. Call run() in a background task.
        3. Call cancel() from outside to set _cancelled flag.
        4. run() checks _cancelled at each phase boundary.
        5. Registry entry is removed in run()'s finally block.
    """

    def __init__(
        self,
        session_id: str,
        *,
        generator_preset: ModelPreset | None = None,
        generator_presets: list[ModelPreset] | None = None,
        editor_preset: ModelPreset | None = None,
        reviewer_presets: list[ModelPreset] | None = None,
        name: str,
        topic: str,
        count: int,
        config: PipelineConfig,
        coverage_mode: str = "none",
        coverage_spec: dict[str, Any] | None = None,
        max_topics_per_question: int = 1,
        context_attachment_id: int | None = None,
        job_id: int | None = None,
        resume_checkpoint: dict[str, Any] | None = None,
        suite_manager=None,
        session_factory=None,
    ):
        self.session_id = session_id
        resolved_generators = list(generator_presets or ([] if generator_preset is None else [generator_preset]))
        if not resolved_generators:
            raise ValueError("at least one generator preset is required")
        self.generator_presets = resolved_generators
        self.generator_preset = self.generator_presets[0]
        self.editor_preset = editor_preset or self.generator_presets[0]
        self.reviewer_presets = reviewer_presets or []
        self.name = name
        self.topic = topic
        self.count = count
        self.config = config
        self.coverage_mode = coverage_mode
        self.coverage_spec = coverage_spec
        self.max_topics_per_question = max_topics_per_question
        self.context_attachment_id = context_attachment_id
        self.job_id = job_id
        self.suite_manager = suite_manager
        self._cancelled = False
        self._pause_for_resume = False
        self._current_phase: str = "init"
        self._context_text: str | None = None
        self.started_at: float = time.time()
        self.saved_count: int = 0

        # Cumulative progress counters (monotonic across batches)
        self._questions_generated: int = 0
        self._questions_reviewed: int = 0
        self._questions_merged: int = 0
        self._log: list[dict[str, Any]] = []
        self._partial_suite: PromptSuite | None = None
        self._partial_error: str | None = None
        self._batch_review_coverage: list[dict[str, Any]] = []
        self._coverage_validation_results: dict[int, dict[str, Any]] = {}
        self._call_started_at: float | None = None
        self._model: str | None = None
        self._batch: int = 0
        self._total_batches: int = 0
        self._completed_generation_batches: int = 0
        self._active_generation_batches: int = 0
        self._reviewers_status: dict[str, dict[str, Any]] = {}
        self._review_batches_in_flight: dict[str, dict[str, Any]] = {}
        self._merge_batches_in_flight: dict[str, dict[str, Any]] = {}
        self._active_generator_name: str | None = None
        self._coverage_plan = None
        self._context_digest: str | None = None
        self._dedupe_report: dict[str, Any] | None = None
        self._generation_calls_in_flight: dict[str, dict[str, Any]] = {}
        self._generation_progress_lock = asyncio.Lock()
        self._review_progress_lock = asyncio.Lock()
        self._merge_progress_lock = asyncio.Lock()
        self._resume_checkpoint: dict[str, Any] = dict(resume_checkpoint or {})
        self._job_checkpoint: dict[str, Any] = self._normalize_checkpoint(self._resume_checkpoint)

        # Late binding: avoid import-time DB connection
        if session_factory is None:
            from app.db.database import SessionLocal  # noqa: PLC0415
            session_factory = SessionLocal
        self.session_factory = session_factory
        self._restore_checkpoint_counters()

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def cancel(self) -> None:
        """Request cooperative cancellation."""
        self._cancelled = True

    def pause_for_resume(self) -> None:
        """Pause cooperatively so the pipeline can be resumed after restart."""
        self._pause_for_resume = True

    async def run(self) -> PromptSuite:
        """Execute the full pipeline and return the saved PromptSuite."""
        active_suite_pipelines[self.session_id] = self
        try:
            await self._persist_job_state(
                status=RunStatus.running,
                phase=self._current_phase,
                snapshot_payload=self.snapshot(),
                checkpoint_payload=self._job_checkpoint,
            )
            return await self._run_pipeline()
        finally:
            active_suite_pipelines.pop(self.session_id, None)

    def _normalize_checkpoint(self, checkpoint: dict[str, Any] | None) -> dict[str, Any]:
        normalized = dict(checkpoint or {})
        normalized.setdefault("generation_results", [])
        normalized.setdefault("review_outcomes", [])
        normalized.setdefault("merged_batches", [])
        normalized.setdefault("prepared_questions", None)
        normalized.setdefault("rubric", None)
        normalized.setdefault("coverage_validation_results", {})
        normalized.setdefault("dedupe_report", None)
        return normalized

    def _restore_checkpoint_counters(self) -> None:
        generation_results = self._job_checkpoint.get("generation_results") or []
        review_outcomes = self._job_checkpoint.get("review_outcomes") or []
        merged_batches = self._job_checkpoint.get("merged_batches") or []
        prepared_questions = self._job_checkpoint.get("prepared_questions")

        self._questions_generated = sum(len(entry.get("questions") or []) for entry in generation_results)
        self._questions_reviewed = sum(len(entry.get("questions") or []) for entry in review_outcomes)
        self._questions_merged = sum(len(entry.get("questions") or []) for entry in merged_batches)
        if prepared_questions:
            self._questions_merged = len(prepared_questions)

        coverage_results = self._job_checkpoint.get("coverage_validation_results") or {}
        self._coverage_validation_results = {
            int(slot_index): dict(result)
            for slot_index, result in coverage_results.items()
        }
        dedupe_report = self._job_checkpoint.get("dedupe_report")
        self._dedupe_report = dict(dedupe_report) if isinstance(dedupe_report, dict) else None

    async def _persist_job_state(
        self,
        *,
        status: RunStatus | object = _UNSET,
        phase: str | object = _UNSET,
        snapshot_payload: dict[str, Any] | object = _UNSET,
        checkpoint_payload: dict[str, Any] | object = _UNSET,
        suite_id: int | None | object = _UNSET,
        partial_suite_id: int | None | object = _UNSET,
        error: str | None | object = _UNSET,
        completed: bool = False,
    ) -> None:
        if self.job_id is None:
            return

        db = self.session_factory()
        try:
            job = db.query(SuiteGenerationJob).filter(SuiteGenerationJob.id == self.job_id).first()
            if job is None:
                return
            if status is not _UNSET:
                job.status = status  # type: ignore[assignment]
            if phase is not _UNSET:
                job.phase = phase  # type: ignore[assignment]
            if snapshot_payload is not _UNSET:
                job.snapshot_payload = snapshot_payload  # type: ignore[assignment]
            if checkpoint_payload is not _UNSET:
                job.checkpoint_payload = checkpoint_payload  # type: ignore[assignment]
            if suite_id is not _UNSET:
                job.suite_id = suite_id  # type: ignore[assignment]
            if partial_suite_id is not _UNSET:
                job.partial_suite_id = partial_suite_id  # type: ignore[assignment]
            if error is not _UNSET:
                job.error = error  # type: ignore[assignment]
            job.updated_at = datetime.now(timezone.utc)
            if completed:
                job.completed_at = datetime.now(timezone.utc)
            db.commit()
        finally:
            db.close()

