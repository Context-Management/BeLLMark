    _NO_REVIEWER_PHASES = ["generate", "dedupe", "repair", "synthesize", "save"]
    _COVERAGE_ALL_PHASES = ["plan", "generate", "review", "merge", "validate", "dedupe", "repair", "synthesize", "save"]
