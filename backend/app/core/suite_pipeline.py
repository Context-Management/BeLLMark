    async def _validate_coverage_batch(
        self,
        questions: list[dict],
        batch_index: int,
        total_batches: int,
    ) -> dict[int, dict[str, Any]]:
        """Call the editor model to classify coverage for a batch of assigned questions."""
        self._batch = batch_index
        self._call_started_at = time.time()
        self._model = self.editor_preset.name
        await self._emit_progress(
            phase="validate",
            detail=f"Validating coverage batch {batch_index}/{total_batches}",
        )
        await self._emit_log(
            "info",
            f"▶ Validate batch {batch_index}/{total_batches} — {self.editor_preset.name}",
        )

        validation_payload = [
            {
                "slot_index": int(question["generation_slot_index"]),
                "system_prompt": question.get("system_prompt", ""),
                "user_prompt": question.get("user_prompt", ""),
                "coverage_topic_ids": list(question.get("coverage_topic_ids") or []),
                "coverage_topic_labels": list(question.get("coverage_topic_labels") or []),
            }
            for question in questions
            if question.get("generation_slot_index") is not None
        ]
        user_prompt = COVERAGE_VALIDATION_USER_TEMPLATE.format(
            count=len(validation_payload),
            coverage_mode=self.coverage_mode,
            context_instruction=self._context_instruction_text(use_digest=True),
            questions_json=json.dumps(validation_payload, indent=2),
        )

        async def do_validate() -> list[dict]:
            result = await generate(
                self.editor_preset,
                COVERAGE_VALIDATION_SYSTEM_PROMPT,
                user_prompt,
                temperature=0.2,
                json_mode=False,
            )
            if not result.get("success"):
                raise PipelineError(
                    f"Coverage validation failed (batch {batch_index}): {result.get('error', 'unknown error')}"
                )
            return await self._parse_json_list(
                result["content"],
                retry_context=f"coverage validate batch {batch_index}",
                retry_system=COVERAGE_VALIDATION_SYSTEM_PROMPT,
                retry_user=user_prompt,
                retry_preset=self.editor_preset,
            )

        validation_rows = await self._call_with_retry(
            do_validate,
            context=f"Coverage validate batch {batch_index}/{total_batches} — {self.editor_preset.name}",
        )

        expected_slot_indexes = {int(question["slot_index"]) for question in validation_payload}
        results: dict[int, dict[str, Any]] = {}
        for row in validation_rows:
            slot_index = row.get("slot_index")
            if slot_index is None:
                slot_index = row.get("generation_slot_index")
            if slot_index is None:
                raise PipelineError(f"Coverage validation batch {batch_index} returned a row without slot_index")
            slot_index = int(slot_index)
            if slot_index not in expected_slot_indexes:
                raise PipelineError(
                    f"Coverage validation batch {batch_index} returned unexpected slot_index {slot_index}"
                )
            coverage_status = row.get("coverage_status") or row.get("label")
            if coverage_status not in {"covered", "partial", "not_covered"}:
                raise PipelineError(
                    f"Coverage validation batch {batch_index} returned invalid status for slot {slot_index}: "
                    f"{coverage_status!r}"
                )
            normalized = dict(row)
            normalized["slot_index"] = slot_index
            normalized["coverage_status"] = coverage_status
            results[slot_index] = normalized

        missing_slot_indexes = expected_slot_indexes.difference(results)
        if missing_slot_indexes:
            raise PipelineError(
                f"Coverage validation batch {batch_index} did not return results for slots "
                f"{sorted(missing_slot_indexes)}"
            )

        elapsed = int(time.time() - (self._call_started_at or time.time()))
        await self._emit_log(
            "info",
            f"✓ Validate batch {batch_index}/{total_batches} complete — {len(results)} results in {self._format_elapsed(elapsed)}",
        )
        return results

    async def _dedupe_questions(self, questions: list[dict]) -> list[dict]:
        """Detect duplicate questions, adjudicate them, and repair removed slots."""
        if len(questions) < 2:
            self._dedupe_report = {
                "candidate_pairs": 0,
                "removed_count": 0,
                "repaired_count": 0,
                "clusters": [],
            }
            return questions

        working_questions = [dict(question) for question in questions]
        dedupe_log: dict[str, Any] = {
            "candidate_pairs": 0,
            "removed_count": 0,
            "repaired_count": 0,
            "clusters": [],
            "repair_rounds": [],
            "unresolved_candidate_pairs": 0,
            "unresolved_duplicate_pairs": 0,
            "unresolved_clusters": [],
        }

        for repair_round in range(2):
            candidate_pairs = find_candidate_duplicate_pairs(working_questions)
            dedupe_log["candidate_pairs"] += len(candidate_pairs)
            if not candidate_pairs:
                break

            adjudications = await self._adjudicate_duplicate_pairs(candidate_pairs, working_questions)
            duplicate_edges: list[tuple[int, int]] = []
            for pair, adjudication in zip(candidate_pairs, adjudications):
                label = adjudication.get("label") or adjudication.get("coverage_status")
                if label in {"duplicate", "near_duplicate"}:
                    duplicate_edges.append(pair)

            if not duplicate_edges:
                break

            clusters = cluster_duplicate_pairs(duplicate_edges)
            if not clusters:
                break

            removed_indices: list[int] = []
            cluster_details: list[dict[str, Any]] = []
            for cluster in clusters:
                survivor_index = max(cluster, key=lambda index: self._dedupe_survivor_key(working_questions[index], index))
                cluster_removed = [index for index in cluster if index != survivor_index]
                if not cluster_removed:
                    continue
                removed_indices.extend(cluster_removed)
                cluster_details.append({
                    "cluster": cluster,
                    "survivor_index": survivor_index,
                    "removed_indices": cluster_removed,
                })

            if not removed_indices:
                break

            dedupe_log["clusters"].extend(cluster_details)
            repair_queue = self._build_repair_queue(working_questions, removed_indices, cluster_details)
            replacements = await self._repair_slots(repair_queue)
            for repair_item, replacement in zip(repair_queue, replacements):
                replace_index = repair_item["replace_index"]
                working_questions[replace_index] = replacement

            dedupe_log["repair_rounds"].append({
                "round": repair_round + 1,
                "removed_count": len(removed_indices),
                "repaired_count": len(replacements),
            })
            dedupe_log["removed_count"] += len(removed_indices)
            dedupe_log["repaired_count"] += len(replacements)
        else:
            dedupe_log["repair_rounds"].append({
                "round": 2,
                "removed_count": 0,
                "repaired_count": 0,
                "exhausted": True,
            })

        remaining_pairs = find_candidate_duplicate_pairs(working_questions)
        dedupe_log["unresolved_candidate_pairs"] = len(remaining_pairs)
        if remaining_pairs:
            remaining_adjudications = await self._adjudicate_duplicate_pairs(remaining_pairs, working_questions)
            unresolved_edges: list[tuple[int, int]] = []
            for pair, adjudication in zip(remaining_pairs, remaining_adjudications):
                label = adjudication.get("label") or adjudication.get("coverage_status")
                if label in {"duplicate", "near_duplicate"}:
                    unresolved_edges.append(pair)
            dedupe_log["unresolved_duplicate_pairs"] = len(unresolved_edges)
            dedupe_log["unresolved_clusters"] = cluster_duplicate_pairs(unresolved_edges)

        self._dedupe_report = dedupe_log
        return working_questions

    async def _adjudicate_duplicate_pairs(
        self,
        candidate_pairs: list[tuple[int, int]],
        questions: list[dict],
    ) -> list[dict]:
        """Ask the editor model to label candidate duplicate pairs."""
        if not candidate_pairs:
            return []

        adjudications: list[dict] = []
        batches = self._split_batches(len(candidate_pairs), batch_size=10)
        offset = 0
        total_batches = len(batches)
        self._total_batches = total_batches
        for batch_index, batch_count in enumerate(batches, start=1):
            batch_pairs = candidate_pairs[offset: offset + batch_count]
            offset += batch_count
            self._batch = batch_index
            self._call_started_at = time.time()
            self._model = self.editor_preset.name
            await self._emit_progress(
                phase="dedupe",
                detail=f"Adjudicating duplicate pairs {batch_index}/{total_batches}",
            )
            await self._emit_log(
                "info",
                f"▶ Dedupe adjudication batch {batch_index}/{total_batches} — {self.editor_preset.name}",
            )

            pair_payload = []
            for pair_index, (left_index, right_index) in enumerate(batch_pairs, start=len(adjudications)):
                left_question = questions[left_index]
                right_question = questions[right_index]
                pair_payload.append({
                    "pair_index": pair_index,
                    "left_index": left_index,
                    "right_index": right_index,
                    "left_prompt_fingerprint": normalized_prompt_fingerprint(
                        left_question.get("system_prompt", ""),
                        left_question.get("user_prompt", ""),
                    ),
                    "right_prompt_fingerprint": normalized_prompt_fingerprint(
                        right_question.get("system_prompt", ""),
                        right_question.get("user_prompt", ""),
                    ),
                    "left_quality_self_score": left_question.get("quality_self_score"),
                    "right_quality_self_score": right_question.get("quality_self_score"),
                    "left_coverage_status": left_question.get("coverage_status"),
                    "right_coverage_status": right_question.get("coverage_status"),
                    "left_user_prompt": left_question.get("user_prompt", ""),
                    "right_user_prompt": right_question.get("user_prompt", ""),
                })

            user_prompt = DEDUPE_ADJUDICATOR_USER_TEMPLATE.format(
                count=len(pair_payload),
                pairs_json=json.dumps(pair_payload, indent=2),
            )

            async def do_adjudicate() -> list[dict]:
                result = await generate(
                    self.editor_preset,
                    DEDUPE_ADJUDICATOR_SYSTEM_PROMPT,
                    user_prompt,
                    temperature=0.2,
                    json_mode=False,
                )
                if not result.get("success"):
                    raise PipelineError(
                        f"Dedupe adjudication failed (batch {batch_index}): {result.get('error', 'unknown error')}"
                    )
                return await self._parse_json_list(
                    result["content"],
                    retry_context=f"dedupe adjudication batch {batch_index}",
                    retry_system=DEDUPE_ADJUDICATOR_SYSTEM_PROMPT,
                    retry_user=user_prompt,
                    retry_preset=self.editor_preset,
                )

            batch_adjudications = await self._call_with_retry(
                do_adjudicate,
                context=f"Dedupe adjudication batch {batch_index}/{total_batches} — {self.editor_preset.name}",
            )
            batch_start_index = len(adjudications)
            rows_by_pair_index = {
                int(row.get("pair_index")): row
                for row in batch_adjudications
                if row.get("pair_index") is not None
            }
            ordered_rows: list[dict[str, Any]] = []
            for pair_index in range(batch_start_index, batch_start_index + len(batch_pairs)):
                row = rows_by_pair_index.get(pair_index)
                if row is None:
                    raise PipelineError(
                        f"Dedupe adjudication batch {batch_index} did not return pair_index {pair_index}"
                    )
                ordered_rows.append(row)
            adjudications.extend(ordered_rows)

        return adjudications

    def _dedupe_survivor_key(self, question: dict, index: int) -> tuple[int, int, int]:
        coverage_rank = {
            "covered": 3,
            "partial": 2,
            "not_covered": 1,
        }
        return (
            coverage_rank.get(question.get("coverage_status"), 0),
            int(question.get("quality_self_score") or 0),
            -index,
        )

    def _build_repair_queue(
        self,
        questions: list[dict],
        removed_indices: list[int],
        cluster_details: list[dict[str, Any]],
    ) -> list[dict[str, Any]]:
        """Build repair targets for removed duplicate questions."""
        if not removed_indices:
            return []

        repair_queue: list[dict[str, Any]] = []
        cluster_by_removed: dict[int, dict[str, Any]] = {}
        for cluster in cluster_details:
            for removed_index in cluster["removed_indices"]:
                cluster_by_removed[removed_index] = cluster

        for removed_index in sorted(removed_indices):
            question = questions[removed_index]
            cluster = cluster_by_removed.get(removed_index, {})
            repair_queue.append({
                "replace_index": removed_index,
                "slot_index": question.get("generation_slot_index", removed_index),
                "system_prompt": question.get("system_prompt", ""),
                "user_prompt": question.get("user_prompt", ""),
                "coverage_topic_ids": list(question.get("coverage_topic_ids") or []),
                "coverage_topic_labels": list(question.get("coverage_topic_labels") or []),
                "coverage_status": question.get("coverage_status"),
                "survivor_index": cluster.get("survivor_index"),
                "survivor_prompt": (
                    questions[cluster["survivor_index"]].get("user_prompt", "")
                    if cluster.get("survivor_index") is not None
                    else ""
                ),
                "banned_prompts": [
                    questions[index].get("user_prompt", "")
                    for index in sorted({removed_index, *(cluster.get("cluster") or [])})
                ],
            })

        return repair_queue

    async def _repair_slots(self, repair_queue: list[dict[str, Any]]) -> list[dict]:
        """Regenerate replacement questions for removed duplicates."""
        if not repair_queue:
            return []

        repaired_questions: list[dict] = []
        batch_size = 4 if self.coverage_mode != "none" else _BATCH_SIZE
        batches = self._split_batches(len(repair_queue), batch_size=batch_size)
        offset = 0
        total_batches = len(batches)
        self._total_batches = total_batches
        for batch_index, batch_count in enumerate(batches, start=1):
            batch_targets = repair_queue[offset: offset + batch_count]
            offset += batch_count
            self._batch = batch_index
            self._call_started_at = time.time()
            self._model = self.generator_preset.name
            await self._emit_progress(
                phase="repair",
                detail=f"Repairing duplicate slots {batch_index}/{total_batches}",
            )
            await self._emit_log(
                "info",
                f"▶ Repairing {len(batch_targets)} duplicate slot(s) — {self.generator_preset.name}",
            )

            user_prompt = REPAIR_GENERATOR_USER_TEMPLATE.format(
                topic=self.topic,
                count=len(batch_targets),
                difficulty=self.config.difficulty,
                repair_mode=self.coverage_mode,
                categories_instruction=self._categories_instruction(),
                generate_answers_instruction=self._answers_instruction(),
                criteria_depth_instruction=self._criteria_depth_instruction(),
                context_instruction=self._context_instruction_text(use_digest=self.coverage_mode != "none"),
                repair_targets_json=json.dumps(batch_targets, indent=2),
                banned_questions_json=json.dumps(
                    [item.get("banned_prompts", []) for item in batch_targets],
                    indent=2,
                ),
            )

            async def do_repair() -> list[dict]:
                result = await generate(
                    self.generator_preset,
                    GENERATOR_SYSTEM_PROMPT,
                    user_prompt,
                    temperature=0.7,
                    json_mode=False,
                )
                if not result.get("success"):
                    raise PipelineError(
                        f"Duplicate repair failed (batch {batch_index}): {result.get('error', 'unknown error')}"
                    )
                return await self._parse_json_list(
                    result["content"],
                    retry_context=f"duplicate repair batch {batch_index}",
                    retry_system=GENERATOR_SYSTEM_PROMPT,
                    retry_user=user_prompt,
                    retry_preset=self.generator_preset,
                )

            batch_repaired = await self._call_with_retry(
                do_repair,
                context=f"Duplicate repair batch {batch_index}/{total_batches} — {self.generator_preset.name}",
            )
            if len(batch_repaired) != len(batch_targets):
                raise PipelineError(
                    f"Duplicate repair batch {batch_index} returned {len(batch_repaired)} questions for "
                    f"{len(batch_targets)} repair targets"
                )

            for repair_target, replacement in zip(batch_targets, batch_repaired):
                repaired = dict(replacement)
                repaired["coverage_topic_ids"] = list(repair_target.get("coverage_topic_ids") or [])
                repaired["coverage_topic_labels"] = list(repair_target.get("coverage_topic_labels") or [])
                repaired["generation_slot_index"] = repair_target.get("slot_index")
                repaired["coverage_status"] = repair_target.get("coverage_status")
                repaired_questions.append(repaired)

        return repaired_questions

    def _build_generation_metadata(self, questions: list[dict]) -> dict[str, Any]:
        return {
            "topic": self.topic,
            "coverage_mode": self.coverage_mode,
            "generator_names": [preset.name for preset in self.generator_presets],
            "editor_name": self.editor_preset.name,
            "reviewer_names": [preset.name for preset in self.reviewer_presets],
            "requested_question_count": self.count,
            "saved_question_count": len(questions),
            "required_leaf_count": len(self._coverage_plan.leaves) if self._coverage_plan else 0,
            "context_attachment_id": self.context_attachment_id,
            "difficulty": self.config.difficulty,
            "criteria_depth": self.config.criteria_depth,
            "generate_answers": self.config.generate_answers,
            "categories": list(self.config.categories or []),
        }

    def _build_coverage_report(self, questions: list[dict]) -> dict[str, Any] | None:
        if self.coverage_mode == "none" or not self._coverage_plan:
            return None

        required_leaves = {leaf.id: leaf.label for leaf in self._coverage_plan.leaves}
        covered_leaf_ids: set[str] = set()
        partial_leaf_ids: set[str] = set()
        slot_results: list[dict[str, Any]] = []

        for question in questions:
            slot_index = question.get("generation_slot_index")
            coverage_topic_ids = list(question.get("coverage_topic_ids") or [])
            coverage_status = question.get("coverage_status")
            if coverage_status is None and slot_index is not None:
                coverage_status = self._coverage_validation_results.get(int(slot_index), {}).get("coverage_status")
            if coverage_status == "covered":
                covered_leaf_ids.update(coverage_topic_ids)
            elif coverage_status == "partial":
                partial_leaf_ids.update(coverage_topic_ids)
            slot_results.append({
                "slot_index": slot_index,
                "coverage_topic_ids": coverage_topic_ids,
                "coverage_topic_labels": list(question.get("coverage_topic_labels") or []),
                "coverage_status": coverage_status,
            })

        uncovered_leaf_ids = sorted(set(required_leaves) - covered_leaf_ids)
        partial_only_leaf_ids = sorted(partial_leaf_ids.difference(covered_leaf_ids))
        return {
            "coverage_mode": self.coverage_mode,
            "required_leaf_count": len(required_leaves),
            "covered_leaf_count": len(covered_leaf_ids),
            "covered_leaf_ids": sorted(covered_leaf_ids),
            "covered_leaf_labels": [required_leaves[leaf_id] for leaf_id in sorted(covered_leaf_ids)],
            "partial_leaf_ids": partial_only_leaf_ids,
            "partial_leaf_labels": [required_leaves[leaf_id] for leaf_id in partial_only_leaf_ids],
            "uncovered_leaf_ids": uncovered_leaf_ids,
            "uncovered_leaf_labels": [required_leaves[leaf_id] for leaf_id in uncovered_leaf_ids],
            "slot_results": slot_results,
        }

    def _acceptance_failures(self, questions: list[dict]) -> list[str]:
        failures: list[str] = []
        coverage_report = self._build_coverage_report(questions)
        if coverage_report and coverage_report["uncovered_leaf_ids"]:
            failures.append(
                f"uncovered required leaves remain: {', '.join(coverage_report['uncovered_leaf_ids'])}"
            )
        unresolved_clusters = (self._dedupe_report or {}).get("unresolved_clusters") or []
        if unresolved_clusters:
            failures.append(f"unresolved duplicate clusters remain: {len(unresolved_clusters)}")
        return failures

    def _coverage_metrics(self) -> tuple[int, int, int]:
        required_leaf_count = len(self._coverage_plan.leaves) if self._coverage_plan else 0
        if required_leaf_count == 0 or not self._coverage_plan:
            return 0, 0, 0

        slots_by_index = {slot.slot_index: slot for slot in self._coverage_plan.slots}
        covered_leaf_ids: set[str] = set()
        for slot_index, result in self._coverage_validation_results.items():
            if result.get("coverage_status") != "covered":
                continue
            slot = slots_by_index.get(int(slot_index))
            if slot is None:
                continue
            covered_leaf_ids.update(slot.required_leaf_ids)

        covered_leaf_count = len(covered_leaf_ids)
        missing_leaf_count = max(required_leaf_count - covered_leaf_count, 0)
        return required_leaf_count, covered_leaf_count, missing_leaf_count

    def _dedupe_metrics(self) -> tuple[int, int]:
        duplicate_cluster_count = len((self._dedupe_report or {}).get("clusters") or [])
        replacement_count = int((self._dedupe_report or {}).get("repaired_count") or 0)
        return duplicate_cluster_count, replacement_count

    async def _synthesize_rubric(self, questions: list[dict]) -> list[dict]:
        """Produce a 3-5 item suite-level rubric from all merged questions."""
        self._call_started_at = time.time()
        self._model = self.editor_preset.name
        await self._emit_progress(
            phase="synthesize",
            detail="Synthesizing suite rubric",
        )
        await self._emit_log(
            "info",
            f"▶ Synthesizing rubric from {len(questions)} questions — {self.editor_preset.name}",
        )

        user_prompt = RUBRIC_USER_TEMPLATE.format(
            count=len(questions),
            questions_json=json.dumps(questions, indent=2),
        )

        async def do_synthesize() -> list[dict]:
            result = await generate(
                self.editor_preset,
                RUBRIC_SYSTEM_PROMPT,
                user_prompt,
                temperature=0.4,
                json_mode=False,
            )
            if not result.get("success"):
                raise PipelineError(
                    f"Rubric synthesis failed: {result.get('error', 'unknown error')}"
                )
            return await self._parse_json_list(
                result["content"],
                retry_context="rubric synthesis",
                retry_system=RUBRIC_SYSTEM_PROMPT,
                retry_user=user_prompt,
                retry_preset=self.editor_preset,
            )

        rubric = await self._call_with_retry(
            do_synthesize,
            context=f"Synthesize rubric — {self.editor_preset.name}",
            on_exhausted="default_rubric",
        )
        elapsed = int(time.time() - (self._call_started_at or time.time()))
        await self._emit_log(
            "info",
            f"✓ Rubric synthesized — {len(rubric)} criteria in {self._format_elapsed(elapsed)}",
        )
        return rubric

    async def _save_suite(
        self, name: str, questions: list[dict], rubric: list[dict]
    ) -> PromptSuite:
        """Persist PromptSuite + PromptSuiteItem rows to the database."""
        self._call_started_at = time.time()
        self._model = None
        await self._emit_progress(
            phase="save",
            detail="Saving suite to database",
        )
        await self._emit_log("info", "▶ Saving suite to database")

        metadata = self._build_generation_metadata(questions)
        description = build_friendly_suite_description(metadata)
        db = self.session_factory()
        try:
            suite = PromptSuite(
                name=name,
                description=description,
                default_criteria=rubric,
                generation_metadata=metadata,
                coverage_report=self._build_coverage_report(questions),
                dedupe_report=self._dedupe_report,
            )
            db.add(suite)
            db.flush()  # get suite.id

            for order, q in enumerate(questions):
                expected_answer = q.get("reference_answer") if self.config.generate_answers else None
                item = PromptSuiteItem(
                    suite_id=suite.id,
                    order=order,
                    system_prompt=q.get("system_prompt", ""),
                    user_prompt=q.get("user_prompt", ""),
                    expected_answer=expected_answer,
                    category=q.get("category"),
                    difficulty=q.get("difficulty"),
                    criteria=q.get("criteria"),
                    coverage_topic_ids=q.get("coverage_topic_ids"),
                    coverage_topic_labels=q.get("coverage_topic_labels"),
                    generation_slot_index=q.get("generation_slot_index"),
                )
                db.add(item)

            db.commit()
            db.refresh(suite)
            self.saved_count = len(questions)

            await self._emit_progress(
                phase="save",
                detail="Suite saved",
            )
            await self._emit_log(
                "info",
                f"✓ Suite saved — ID {suite.id}, {len(questions)} questions",
            )

            return suite
        except Exception:
            db.rollback()
            raise
        finally:
            db.close()

