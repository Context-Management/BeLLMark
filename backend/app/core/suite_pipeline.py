    async def _parse_json_list(
        self,
        content: str,
        retry_context: str,
        retry_system: str,
        retry_user: str,
        retry_preset: ModelPreset | None = None,
    ) -> list[dict]:
        """
        Parse a JSON array from an LLM response.

        Strategy:
        1. Strip markdown fences (```json ... ```)
        2. Slice from first '[' to last ']'
        3. json.loads()
        4. On failure: retry once with stricter prompt using the same model
        """
        parsed = self._try_parse(content)
        if parsed is not None:
            return parsed

        await self._emit_log(
            "warning",
            "⟳ JSON parse failed, retrying with strict prompt (within same call budget)",
        )
        # Retry once with a stricter prompt using the same model that produced the response
        preset = retry_preset or self.generator_preset
        strict_user = (
            "Your previous response was not valid JSON. "
            "Return ONLY the raw JSON array — no markdown fences, "
            "no explanation, no preamble. Start your response with '[' "
            "and end with ']'.\n\n"
            + retry_user
        )
        retry_result = await generate(
            preset,
            retry_system,
            strict_user,
            temperature=0.2,
            json_mode=False,
        )

        if retry_result.get("success"):
            parsed = self._try_parse(retry_result["content"])
            if parsed is not None:
                return parsed

        raise PipelineError(
            f"Failed to parse JSON array after retry ({retry_context}). "
            f"Raw content: {content[:300]!r}"
        )

    @staticmethod
    def _try_parse(content: str) -> list[dict] | None:
        """Attempt to parse content as a JSON array; return None on failure."""
        if not content:
            return None
        # Strip markdown fences
        stripped = re.sub(r"^```(?:json)?\s*", "", content.strip(), flags=re.MULTILINE)
        stripped = re.sub(r"\s*```\s*$", "", stripped.strip(), flags=re.MULTILINE)
        stripped = stripped.strip()

        # Find JSON array boundaries
        start = stripped.find("[")
        end = stripped.rfind("]")
        if start == -1 or end == -1 or end <= start:
            return None

        try:
            result = json.loads(stripped[start : end + 1])
            if isinstance(result, list):
                return result
            return None
        except (json.JSONDecodeError, ValueError):
            return None

